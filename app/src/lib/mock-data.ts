// Generate mock candle data
function generateCandles(basePrice: number, count: number): CandleData[] {
  const candles: CandleData[] = [];
  const now = Math.floor(Date.now() / 1000);
  // Use seeded random for deterministic charts (same on every render)
  let seed = Math.abs(Math.round(basePrice * 1000000));
  function seededRandom() {
    seed = (seed * 1103515245 + 12345) & 0x7fffffff;
    return seed / 0x7fffffff;
  }
  // Pre-generate raw noise, then rescale so the path ends exactly at basePrice
  const startPrice = basePrice * 0.92;
  const rawNoise: number[] = [];
  let cumulative = 0;
  for (let i = 0; i < count; i++) {
    const n = (seededRandom() - 0.5) * basePrice * 0.02;
    rawNoise.push(n);
    cumulative += n;
  }
  // Correction: spread the error evenly so final close = basePrice
  const totalNeeded = basePrice - startPrice;
  const correction = (totalNeeded - cumulative) / count;
  let price = startPrice;
  for (let i = 0; i < count; i++) {
    const open = price;
    const move = rawNoise[i] + correction;
    const close = open + move;
    const wick = basePrice * 0.004;
    const high = Math.max(open, close) + seededRandom() * wick;
    const low = Math.min(open, close) - seededRandom() * wick;
    price = close;
    candles.push({
      time: now - (count - i) * 3600,
      open,
      high,
      low,
      close,
    });
  }
  return candles;
}
